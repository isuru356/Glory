package Model;

/**
 *
 * @author I S Arandara
 */
public class Players 
{
    public String name;
    public String score;

    public Players(String name, String score) {
        this.name = name;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
